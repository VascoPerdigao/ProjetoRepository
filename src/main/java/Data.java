public class Data {
    private Integer ano;
    private Integer mes;
    private Integer dia;

    public Data(Integer ano, Integer mes, Integer dia) {
        this.ano = ano;
        this.mes = mes;
        this.dia = dia;
    }

    public Integer getAno() {
        return ano;
    }

    public Integer getMes() {
        return mes;
    }

    public Integer getDia() {
        return dia;
    }
}
