public class Atleta {
    private String nome;
    private Integer atleta_ID;
    private Character genero;
    private String País;
    private Data dta_nascimento;
    private Integer contacto;

    public Atleta(String nome, Integer atleta_ID, Character genero, String país, Data dta_nascimento, Integer contacto) {
        this.nome = nome;
        this.atleta_ID = atleta_ID;
        this.genero = genero;
        País = país;
        this.dta_nascimento = dta_nascimento;
        this.contacto = contacto;
    }


}
